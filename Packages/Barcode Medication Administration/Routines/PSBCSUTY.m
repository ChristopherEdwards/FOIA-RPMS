GETADMX ;
 ;
 S (PSBXX,PSBXXX)="" F  S PSBXX=$O(^PSB(53.79,"AORDX",PSBDFN,PSBONX,PSBXX)) Q:PSBXX=""  D
 .Q:(PSBXX<PSBMHBCK)&'PSBLVIV
