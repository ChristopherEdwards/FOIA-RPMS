 S ACMU11=ACMU1\2+(ACMU1#2)
 F ACM=1:1:ACMU11 D
 .S ACMU1=ACM,ACMY=ACMZ(ACMU1)
 .W !,?10,$J(ACMU1,3)_")",?$X+2,ACMX(ACMU1)
 .I $D(^ACM(41.1,ACMRG,2,ACMY))
 .S ACMU1=ACM+ACMU11
 .;Q:'$D(ACMZ(ACMU1))  ;S ACMU1=ACMU1-1 Q
 .I '$D(ACMZ(ACMU1)) S ACMU1=ACMU1-1 Q
 .S ACMY=ACMZ(ACMU1)
