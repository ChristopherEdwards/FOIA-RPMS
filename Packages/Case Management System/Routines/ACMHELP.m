 .I $D(^ACM(41.1,ACMRG,2,ACMY))
 ;S ACMU1=ACMU1+1
 ;I ACMU1#2 W !,?10
 ;W ?45
 ;W $J(ACMU1,3)_")"," All data types"
 ;Q
