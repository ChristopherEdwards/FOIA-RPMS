EN ;EP -- MAIN ENTRY POINT
 ;
 D PCOM
 Q
PCOM ;----- COMPILE PRINT TEMPLATES
 ;
 N J,DMAX,X,Y,Z
 D ^XBKVAR
 D HOME^%ZIS
 D BMES^XPDUTL("COMPILING PRINT TEMPLATES...")
 F J=1:1 S Z=$T(PTMP+J) Q:Z["$$END"  D
 . S Y=$P(Z,";",3)
 . S Y=$O(^DIPT("B",Y,0))
 . Q:'Y
 . S X=$P(Z,";",4)
 . S DMAX=5000
 . D EN^DIPZ
 Q
PTMP ;----- PRINT TEMPLATES TO BE COMPILED
 ;;ACR TRAVEL ORDER;ACRPTO
 ;;ACR TRAVEL ORDER SUMMARY;ACRPTOS
 ;;ACR TRAVEL VOUCHER;ACRPTV
 ;;ACR TRAVEL VOUCHER SUMMARY;ACRPTVS
 ;;ACR TRAINING 350;ACRPTRG
 ;;ACR TRAINING REQUEST DISPLAY;ACR148
 ;;$$END
 Q
