 ;;ROUTINE TO EDIT PAYROLL PAY PERIOD
EN ;EP;
 S ACRPP=$P(^ATAEMP($P(^ACRDOC(ACRDOCDA,"PR"),U),0),U,9)
 S DIR(0)="N^1:"_$S(ACRPP'="CO":53,1:12)
 S DIR("A")="PAY PERIOD "
 S DIR("B")=$P(^ACRDOC(ACRDOCDA,"PR"),U,2)
 D DIR^ACRFDIC
 Q:'Y
 S DA=ACRDOCDA
 S DIE="^ACRDOC("
 S DR="999020////"_Y
 D DIE^ACRFDIC
EXIT K ACRPP
 Q
