 ;      VERSION 2.0T2 POST INSTALL ROUTINE
 ;
EN ;EP -- MAIN ENTRY POINT
 ;
 D OPT
 D FUNC
 Q
OPT ;----- RENAME OPTION
 ;
 ;      RENAME OPTION ACRN P PAYMENTS TO
 ;                    ACRF P PAYMENTS
 ;
 N DIC,X,Y
 S DIC="^DIC(19,"
 S DIC(0)=""
 S X="ACRN P PAYMENTS"
 D ^DIC
 Q:+Y'>0
 D BMES^XPDUTL("Renaming option ACRN P PAYMENTS to ACRF P PAYMENTS")
 S DA=+Y
 S DIE=DIC
 S DR=".01///^S X=""ACRF P PAYMENTS"""
 D ^DIE
 Q
FUNC ;----- DELETE FUNCTION
 ;
 S DIC="^DD(""FUNC"","
 S DIC(0)=""
 S X="ACRZLASTPMT"
 D ^DIC
 Q:+Y'>1
 S DA=+Y
 S DIE=DIC
 S DR=".01///^S X=""@"""
 D ^DIE
 ;
 Q
