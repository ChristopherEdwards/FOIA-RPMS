 ;;2.0;DIABETES MANAGEMENT SYSTEM;**3**;JUN 14, 2007
 ;;UTILITY ROUTINE USED TO MANAGE ALL INTERFACE TO FILEMAN
 ;;WITH FEW EXCEPTIONS, THIS IS THE ONLY PLACE IN ARMS FROM WHICH
 ;;FILEMAN ENTRY POINTS ARE CALLED
DIC ;EP;
 Q:$D(BDMLLTOT)
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D ^DIC
 S:$D(DUOUT) BDMLLTQT=""
 S:$D(DTOUT)!(X="^^") (BDMLLTQT,BDMLLTOT)=""
 K DIC,DA,DD,DR,DINUM,D,DLAYGO,DIADD ;IHS/CMI/THL PATCH 4 ADD 'DIADD' TO KILL LIST
 Q
MIX ;EP;
 Q:$D(BDMLLTOT)
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D MIX^DIC1
 S:$D(DTOUT)!$D(DUOUT)!(X="^^") (BDMLLTQT,BDMLLTOT)=""
 K DIC,DA,DD,DR,DINUM,D,DLAYGO
 Q
IX ;EP;
 Q:$D(BDMLLTOT)
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D IX^DIC
 S:$D(DTOUT)!$D(DUOUT)!(X="^^") (BDMLLTQT,BDMLLTOT)=""
 K DIC,DA,DD,DR,DINUM,D,DLAYGO
 Q
DIE ;EP;
 Q:$D(BDMLLTOT)
 Q:'DA
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 L +@(DIE_DA_")"):4 I '$T S BDMLLTQT="" Q:'$D(IOST)#2  W:$E(IOST,1,2)="C-" !!,"Entry being edited by another user.  Please try Later.",! H 3 Q
 S BDMLDIDA=DA
 D ^DIE
 L -@(DIE_BDMLDIDA_")"):0
 I $D(DTOUT)!$D(DUOUT) S (BDMLLTQT,BDMLLTOT)=""
 K DIE,DA,DR,BDMLDIDA
 Q
DDS ;EP;FOR SCREENMAN CALLS
 S DDSFILE=DIE
 D ^DDS
 K DDSFILE,BDMLCREN
 Q
FILE ;EP;
 K DD,DO,DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D FILE^DICN
 S:$D(DTOUT)!$D(DUOUT)!(X="^^") (BDMLLTQT,BDMLLTOT)=""
 K DIC,DA,DD,DR,DINUM,DLAYGO
 Q
DIR ;EP;
 I $D(BDMLLTOT) K DIR S Y="" Q
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D ^DIR
 S BDMLLTY=Y
 S:$D(DIRUT)!$D(DIROUT)!$D(DUOUT) BDMLLTQT=""
 S:X="^^"!$D(DTOUT) (BDMLLTQT,BDMLLTOT)=""
 K DIR,DIRUT,DIROUT,DUOUT,DTOUT
 Q
DIK ;EP;
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D ^DIK
 K DIK
 Q
IX1 ;EP;
 Q:$D(BDMLLTOT)
 K DTOUT,DUOUT,BDMLLTQT,BDMLLTOT
 D IX1^DIK
 K DIK,DA
 Q
DIQ1 ;EP;
 D EN^DIQ1
 K DIC,DA,DR,DIQ
 Q
WP ;EP;TO BROWSE WORD PROCESSING FIELD
 Q:'$D(BDMLFILE)#2!'$D(BDMLIEN)#2!'$D(BDMLFIEL)
 K ACMSCREN
 D WP^DDBR(BDMLFILE,BDMLIEN,BDMLFIEL,"N",$G(BDMLTITL))
 Q
