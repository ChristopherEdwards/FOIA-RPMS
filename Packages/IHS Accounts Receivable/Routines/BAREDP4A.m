 . S BARRET=0
 . S BARXBILL=BARBILL
 . F IXX=1:1 Q:$E(BARXBILL,1)'="0"  D  Q:BARRET
 . . S BARXBILL=$E(BARXBILL,2,999) I BARXBILL=""  Q
 . . I $D(^BARBL(DUZ(2),"G",BARXBILL)) S BARRET="1^"_BARXBILL
 . . Q
 . Q
    I BARBILL=+BARBILL D  Q BARRET
    . S BARXBILL=+BARBILL
