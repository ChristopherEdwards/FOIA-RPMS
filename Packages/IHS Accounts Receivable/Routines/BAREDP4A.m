 . S BARTBTCH=$$GET1^DIQ(90050.03,BARTR,14)
 . S BARTITEM=$$GET1^DIQ(90050.03,BARTR,15)
 . I BARTBTCH'="",BARITEM'="",BARBTCH=BARTBTCH,BARITEM=BARTITEM S BARTFLG=1
 I BARTFLG S ERRORS("HTRN")=""
