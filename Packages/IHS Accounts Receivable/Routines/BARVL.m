 W !,"'BAR DISPLAY' List Template..."
 S DA=$O(^SD(409.61,"B","BAR DISPLAY",0))
 S DIK="^SD(409.61,"
 D ^DIK:DA
 K DO,DD
 S DIC(0)="L"
 S DIC="^SD(409.61,"
 S X="BAR DISPLAY"
 K DD,DO
 D FILE^DICN
 S VALM=+Y
 I VALM>0 D
 .S ^SD(409.61,VALM,0)="BAR DISPLAY^2^^132^3^20^1^1^DISPLAY^^BAR DISPLAY^1^^1"
 .S ^SD(409.61,VALM,1)="^VALM HIDDEN ACTIONS"
 .S ^SD(409.61,VALM,"ARRAY")=" ^TMP(""BAR"",$J)"
 .S ^SD(409.61,VALM,"FNL")="D EXIT^BARDSP"
 .S ^SD(409.61,VALM,"HDR")="D HDR^BARDSP"
 .S ^SD(409.61,VALM,"HLP")="D HELP^BARDSP"
 .S ^SD(409.61,VALM,"INIT")="D INIT^BARDSP"
 .S DA=VALM
 .S DIK="^SD(409.61,"
 .D IX1^DIK
 .K DA,DIK
 .W "Filed."
 ;
 K DIC,DIK,VALM,X,DA
 Q
