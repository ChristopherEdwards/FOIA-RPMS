ASDAVAIL ; IHS/ADC/PDW/ENM - CANCEL/RESTORE CL AVAIL ; [ 03/25/1999  11:48 AM ]
 ;;5.0;IHS SCHEDULING;;MAR 25, 1999
 ;
ASK ;
 NEW DIR
 S DIR(0)="SO^C:CANCEL CLINIC AVAILABILITY;R:RESTORE CLINIC AVAILABILITY"
 S DIR("A")="Select Action" D ^DIR Q:$D(DIRUT)  Q:Y=-1  D @Y G ASK
 ;
C ; call cancel
 D ^SDC D SDC^SDKILL Q
 ;
R ; call restore
 D ^SDUNC D SDUNC^SDKILL Q
