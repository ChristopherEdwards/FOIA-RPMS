 ;
ASK ;
 NEW DIR
 S DIR(0)="SO^I:INACTIVATE CLINIC;R:REACTIVATE CLINIC"
 S DIR("A")="Select Action" D ^DIR Q:$D(DIRUT)  Q:Y=-1  D @Y G ASK
 ;
I ; call inactivate
 D ^SDINACT D SDINACT^SDKILL Q
 ;
R ; call reactivate
 D ^SDREACT D SDREACT^SDKILL Q
