ZAPINA ;EP
 D E1,E2
 Q
E1 ;REMOVE INACTIVE DATE FOR ADA RECORD 4268
 S ADEIRN=""
 S ADEIRN=$O(^AUTTADA("B",4268,0)) I +ADEIRN D ZAPIT
 K D,DA,DR,DIE,D0,DIC,ADEIRN
 Q
ZAPIT ;
 S DIE="^AUTTADA(",DA=ADEIRN
 S DR=".08////@" D ^DIE
 Q
E2 ;Remove Synonym and Mnemonic from 4 records in the Dent Operative Site File
 S ADEORN=""
 S ADEORN=$O(^ADEOPS("B","SUPRANUMERY #1",0)) I +ADEORN D ZAPOS
 S ADEORN=$O(^ADEOPS("B","SUPRANUMERY #2",0)) I +ADEORN D ZAPOS
 S ADEORN=$O(^ADEOPS("B","SUPRANUMERY #3",0)) I +ADEORN D ZAPOS
 S ADEORN=$O(^ADEOPS("B","SUPRANUMERY #4",0)) I +ADEORN D ZAPOS
 K D,DA,DR,DIE,D0,DIC,ADEORN
 Q
ZAPOS ;
 S DIE="^ADEOPS(",DA=ADEORN
 S DR="8801////@"_";8802////@" D ^DIE
 Q
