 .. S BGPRO=$G(^BGPTMP("TAX",$J,BGPDA,BGPIEN))
 .. S ^BGPTMP($J,BGPI)=BGPIEN_"("_$P(BGPRO,U)_"/"_$P(BGPRO,U,2)_"/"_BGPDA_")"_$C(30)
