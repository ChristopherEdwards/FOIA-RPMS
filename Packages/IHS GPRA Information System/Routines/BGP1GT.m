 S X="MERR^BGP1GU",@^%ZOSF("TRAP") ; m error trap
 N BGPDA,BGPI,P,BGPPKG,BGPPKGI,BGPI
 S P="|"
 K ^BGPTMP($J)
 S RETVAL="^BGPTMP("_$J_")"
 S BGPI=0
 S BGPERR=""
 S BGPPKG=$P(BGPSTR,P)
 S BGPPKGI=$O(^DIC(9.4,"C",BGPPKG,0))
