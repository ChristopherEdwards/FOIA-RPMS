BGP3DELP ; IHS/CMI/LAB - IHS HEDIS print ;
 ;;13.0;IHS CLINICAL REPORTING;;NOV 20, 2012;Build 81
 ;
 ;
PRINT ;
 K ^TMP($J)
 S BGPIOSL=$S($G(BGPGUI):55,1:IOSL)
 I BGPROT="D" G DEL
 S BGPPTYPE="P"
 D ^BGP3ELH
 S BGPGPG=0
 S BGPQUIT=""
 D PRINT1
 K ^TMP($J)
 I BGPROT="P" K ^XTMP("BGP3D",BGPJ,BGPH),^TMP($J) Q
 ;
DEL ;create delimited output file
 D ^%ZISC ;close printer device
 K ^TMP($J)
 S BGPPTYPE="D"
 D ^BGP3ELL ;create ^tmp of delimited report
 K ^XTMP("BGP3D",BGPJ,BGPH)
 K ^TMP($J)
 Q
WP ;
 K ^UTILITY($J,"W")
 S BGPZ=0,BGPLCNT=0
 S DIWL=1,DIWR=80,DIWF="" F  S BGPZ=$O(^BGPELIH(BGPIC,BGPNODE,BGPY,1,BGPZ)) Q:BGPZ'=+BGPZ  D
 .S BGPLCNT=BGPLCNT+1
 .S X=^BGPELIH(BGPIC,BGPNODE,BGPY,1,BGPZ,0) S:BGPLCNT=1 X=" - "_X D ^DIWP
 .Q
WPS ;
 S Z=0 F  S Z=$O(^UTILITY($J,"W",DIWL,Z)) Q:Z'=+Z  D
 .I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 .;D W^BGP3DP(^UTILITY($J,"W",DIWL,Z,0),0,1,BGPPTYPE)
 .W !,^UTILITY($J,"W",DIWL,Z,0)
 K DIWL,DIWR,DIWF,Z
 K ^UTILITY($J,"W"),X
 Q
 ;
PRINT1 ;EP
 S BGPIC=0 F  S BGPIC=$O(BGPIND(BGPIC)) Q:BGPIC=""!(BGPQUIT)  D
 .D HEADER^BGP3DPH ;header for all measures
 .I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 .W !,$P(^BGPELIH(BGPIC,0),U,3),!
 .I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 .W !,"Denominator(s):"
 .S BGPNODE=61
 .S BGPX=0 F  S BGPX=$O(^BGPELIH(BGPIC,61,"B",BGPX)) Q:BGPX'=+BGPX!(BGPQUIT)  D
 ..S BGPY=0 F  S BGPY=$O(^BGPELIH(BGPIC,61,"B",BGPX,BGPY)) Q:BGPY'=+BGPY!(BGPQUIT)  D
 ...;S BGPZ=0 F  S BGPZ=$O(^BGPELIH(BGPIC,61,BGPY,1,BGPZ)) Q:BGPZ'=+BGPZ!(BGPQUIT)  D
 ...;I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 ...D WP ;W !,^BGPELIH(BGPIC,61,BGPY,1,BGPZ,0)
 ...Q
 ...;W !
 ...Q
 ..Q
 .I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 .W !!,"Numerator(s):"
 .S BGPNODE=62
 .S BGPX=0 F  S BGPX=$O(^BGPELIH(BGPIC,62,"B",BGPX)) Q:BGPX'=+BGPX!(BGPQUIT)  D
 ..S BGPY=0 F  S BGPY=$O(^BGPELIH(BGPIC,62,"B",BGPX,BGPY)) Q:BGPY'=+BGPY!(BGPQUIT)  D
 ...;S BGPZ=0 F  S BGPZ=$O(^BGPELIH(BGPIC,62,BGPY,1,BGPZ)) Q:BGPZ'=+BGPZ!(BGPQUIT)  D
 ...;.I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 ...;.W !,^BGPELIH(BGPIC,62,BGPY,1,BGPZ,0)
 ...D WP  ;.Q
 ...Q
 ..Q
 .I $O(^BGPELIH(BGPIC,11,0)) W !!,"Logic" S BGPX=0 F  S BGPX=$O(^BGPELIH(BGPIC,11,BGPX)) Q:BGPX'=+BGPX  D
 ..I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 ..W !,^BGPELIH(BGPIC,11,BGPX,0)
 .I $O(^BGPELIH(BGPIC,51,0)) W !!,"Performance Measure Description" S BGPX=0 F  S BGPX=$O(^BGPELIH(BGPIC,51,BGPX)) Q:BGPX'=+BGPX  D
 ..I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 ..W !,^BGPELIH(BGPIC,51,BGPX,0)
 .I $O(^BGPELIH(BGPIC,52,0)) W !!,"Past Performance and/or Target" S BGPX=0 F  S BGPX=$O(^BGPELIH(BGPIC,52,BGPX)) Q:BGPX'=+BGPX  D
 ..I $Y>(BGPIOSL-3) D HEADER^BGP3DPH Q:BGPQUIT
 ..W !,^BGPELIH(BGPIC,52,BGPX,0)
 .X ^BGPELIH(BGPIC,3)
 ;
 D ^BGP3ELS
 D EXIT
 Q
EXIT ;
 I $E(IOST)="C",IO=IO(0),'$D(ZTQUEUED) W ! S DIR(0)="EO",DIR("A")="End of report.  Press ENTER" D ^DIR K DIR S:$D(DUOUT) DIRUT=1
 Q
C(X,X2,X3) ;
 D COMMA^%DTC
 Q X
