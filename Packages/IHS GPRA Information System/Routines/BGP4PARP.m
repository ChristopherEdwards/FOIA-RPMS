 ;
 ;
PRINT ;EP
 I $G(BGPDASH) D AREA^BGP4DBPR Q
 K ^TMP($J)
 S BGPIOSL=$S($G(BGPGUI):55,1:IOSL)
 S BGPGPG=0
 S BGPQUIT=""
 S BGPIFTR=""
 S ^TMP($J,"BGPDEL",0)=0
 S BGPPTYPE="P"
 D SETEXCEL^BGP4DP
 I BGPROT="D" G DEL
 D AREACP^BGP4DH
 S BGPQUIT="",BGPGPG=0,BGPRPT=0
 D PRINT1^BGP4DP
 Q:BGPQUIT
 S BGPIFTR=1
 Q:BGPROT="P"
DEL ;create delimited output file
 S BGPQUIT="",BGPGPG=0,BGPRPT=0,BGPIFTR=""
 D ^%ZISC ;close printer device
 K ^TMP($J)
 ;D ^BGP4PDL ;create ^tmp of delimited report
 S ^TMP($J,"BGPDEL",0)=0
 S BGPPTYPE="D",BGPQUIT=0
 D AREACP^BGP4DH
 S BGPQUIT="",BGPGPG=0,BGPRPT=0
 D PRINT1^BGP4DP
 I '$G(BGPNODEL) D SAVEDEL^BGP4PDL   ;D ^BGP4PDL ;create ^tmp of delimited report
 S BGPIFTR=1
 K ^TMP($J)
 Q
 ;
