PRINT ;EP
 K ^TMP($J)
 S BGPIOSL=$S($G(BGPGUI):55,1:IOSL)
 S BGPGPG=0
 S BGPQUIT=""
 S BGPIFTR=""
 D SETEXCEL^BGP8DP
 I BGPROT="D" G DEL
 D AREACP^BGP8DH
 S BGPQUIT="",BGPGPG=0,BGPRPT=0
 D PRINT1^BGP8DP
 Q:BGPQUIT
 Q:BGPROT="P"
DEL ;create delimited output file
 S BGPQUIT="",BGPGPG=0,BGPRPT=0
 D ^%ZISC ;close printer device
 K ^TMP($J)
 D ^BGP8PDL ;create ^tmp of delimited report
 S BGPIFTR=1
 Q
 ;
