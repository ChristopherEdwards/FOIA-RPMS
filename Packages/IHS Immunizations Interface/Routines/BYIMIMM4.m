DFN(UIF) ;FIND PATIENT DFN
 N X,Y,Z,LOC,HRN,DFN
 S X=$P($P($G(^INTHU(UIF,3,2,0)),"|",4),U)
 S LOC=$E(X,1,6)
 S HRN=+$E(X,7,12)
 Q:'LOC!'HRN ""
 S LOC=$O(^AUTTLOC("C",LOC,0))
 Q:'LOC ""
 S DFN=""
 S X=0
 F  S X=$O(^AUPNPAT("D",HRN,X)) Q:'X!DFN  S Y=0 F  S Y=$O(^AUPNPAT("D",HRN,X,Y)) Q:'Y!DFN  S:Y=LOC DFN=X
 Q DFN
