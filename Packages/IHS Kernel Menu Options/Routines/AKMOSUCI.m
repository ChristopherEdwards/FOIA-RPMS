AKMOSUCI ; SWITCH UCIs FROM MENU [ 03/04/86  8:12 AM ]
 ;;2.0;IHS KERNEL UTILITIES;;JUN 28, 1993
 R !,"UCI: ",AUCINM
 Q:AUCINM=""
 I '$D(^DIC(3,DUZ,.2,"B",AUCINM)) W !,"Not allowed!" Q
 S AUCISEQ="",AUCISEQ=$O(^DIC(3,DUZ,.2,"B",AUCINM,AUCISEQ))
 Q:AUCISEQ=""
 S AUCIPGM=$P(^DIC(3,DUZ,.2,AUCISEQ,0),U,2)
 Q:AUCIPGM=""
 S X=AUCINM D ^AUXUCI
 G @(U_AUCIPGM)
 Q
