 K DIR
 S DIR("B")="SAME"
 S DIR("A")="Select GUARANTOR"
 S DIR(0)="FO^1:30"
 D ^DIR
 I $D(DTOUT)!$D(DUOUT)!$D(DIROUT) S EXIT=1 Q
 S X=Y
 S SAME=X="SAME"
 I X="SAME" D SAME(DFN) Q
