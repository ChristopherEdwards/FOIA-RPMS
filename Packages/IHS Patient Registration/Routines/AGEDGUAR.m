GETVDIC(VROOT) ;EP - PROCESS VAR PTR FLD
 Q:VROOT="" ""
 S VROOT=U_VROOT_"0)"
 Q +$P($G(@VROOT),U,2)
WMSG ;EP - MSG TO DISP IF NO ENTRY IN FILE
 W !,"You must first enter a GUARANTOR"
