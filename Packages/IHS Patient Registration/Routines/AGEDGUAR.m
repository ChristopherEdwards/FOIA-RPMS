 ;;DATE OF BIRTH^?3^9000043.0101^.04^!^4^EDITDOB^
 ;;GENDER^?3^9000043.0101^.05^!^5^EDITSEX
 ;;-Effective Date--------------------------Expiration Date------------------------
 ;;^?0^9000043.0111^.01^!?0^^NEWEFFDT
 ;;-GUARANTOR ADDRESS--------------------------------------------------------------
