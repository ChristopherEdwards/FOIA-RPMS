BHLZPRI ; cmi/sitka/maw - BHL File Inbound ZPR Segment ; 
 ;;3.01;BHL IHS Interfaces with GIS;;JUL 01, 2001
 ;
 ;this routine will file the inbound ZPR segment, it is called from
 ;BHLPR1I
 ;
 Q
 ;
DEN ;EP - dental procedure
 N BHLR
 S BHLR="ZPR"
 S BHLNOU=$G(@BHLTMP@(BHLDA,1))
 S BHLOPS=$P($G(@BHLTMP@(BHLDA,2)),CS,2)
 S BHLTS=$G(@BHLTMP@(BHLDA,3))
 S APCDALVR("APCDTNOU")=BHLNOU
 S APCDALVR("APCDTOS")=BHLOPS
 S APCDALVR("APCDTSUR")=BHLTS
 Q
 ;
PRC ;EP - procedure
 S BHLPVN=$G(@BHLTMP@(BHLDA,4))
 S BHLINF=$G(@BHLTMP@(BHLDA,5))
 S BHLAA=$G(@BHLTMP@(BHLDA,6))
 S BHLASA=$P($G(@BHLTMP@(BHLDA,7)),CS,2)
 S BHLPRC=$P($G(@BHLTMP@(BHLDA,8)),CS)
 S APCDALVR("APCDTNQ")=BHLPVN
 S APCDALVR("APCDINF")=BHLINF
 S APCDALVR("APCDTAA")=BHLAA
 S APCDALVR("APCDTAPC")=BHLASA
 S APCDALVR("APCDTCPT")=BHLPRC
 Q
 ;
