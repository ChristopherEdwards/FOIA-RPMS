 I $G(BUDT6) S T="TAXS" F J=1:1 S Z=$T(@T+J),BUDX=$P(Z,";;",2),Y=$P(Z,";;",3) Q:BUDX=""  D
 .I '$D(^ATXAX("B",BUDX)) S A(BUDX)=Y_"^is Missing" Q
 .S I=$O(^ATXAX("B",BUDX,0))
