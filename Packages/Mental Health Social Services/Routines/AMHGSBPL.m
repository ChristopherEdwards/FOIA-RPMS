 . S RET1=$$UPNT("A",AMHIEN,"",AMHPAT,AMHNT,AMHPNU,AMHSLT)
 . I '$G(RET1) S ^AMHTMP($J,AMHI)="0~Error Adding Note"_$C(30)
 S ^AMHTMP($J,AMHI+1)=$C(31)
