 S AMHI=AMHI+1
 I $G(RET) D
 . S AMHIEN=RET
 . D PLU^AMHAPRB(AMHIEN,AMHVST,AMHPAT,AMHDU,AMHPU)
 . S ^AMHTMP($J,AMHI)=RET_$C(30)
