 I AMHMD="D" D  Q
 . N DFDA,DFIENS,DERR
 . S DFIENS=AMHIEN_","
 . S DFDA(9000011,DFIENS,.12)="D"
 . S DFDA(9000011,DFIENS,2.01)=DUZ
