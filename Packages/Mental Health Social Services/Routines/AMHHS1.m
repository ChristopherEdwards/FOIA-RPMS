GETNARR ;
 I APCHSNRQ]"" S APCHSNRQ=$S($D(^AUTNPOV(APCHSNRQ)):$P(^AUTNPOV(APCHSNRQ,0),U),1:"***** "_APCHSNRQ_" *****")
 E  S APCHSNRQ=""
 Q
