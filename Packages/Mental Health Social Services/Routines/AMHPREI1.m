 .I $P(^AMHPROB(AMHDA,0),U,2)=AMHD Q
 .D ^XBFMK
 .S DA=AMHDA,DIE="^AMHPROB(",DR=".02///"_AMHD D ^DIE
 .I $D(Y) W !,"Updating ICD code for ",D," failed die." Q
 .K DIE,DA,DR D ^XBFMK
 .Q
