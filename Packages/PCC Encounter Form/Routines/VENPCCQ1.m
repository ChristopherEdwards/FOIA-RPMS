 F PCE=1:1:MAX D
 . S STG0=$P(LSTG0,"|",PCE) I STG0="" Q
 . S STG1=$P(LSTG1,"|",PCE) I STG1="" Q
 . S X=""""_DFN_""""
 . D ^DIC I Y=-1 Q
 . I 'START S START=+Y
 . S ^VEN(7.15,+Y,0)=STG0
 . S ^VEN(7.15,+Y,1)=STG1
 . S FIN=+Y
 . Q
 Q
