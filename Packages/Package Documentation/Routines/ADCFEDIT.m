ADCFEDIT ;THL/IHS-OIRM-DSD;EDIT OF OPTION DESCRIPTIONS FOR THE DOCUMENTATION UTILITIES [ 03/21/91  8:10 AM ]
 ;;1;PACKAGE DOCUMENTATION UTILITIES
EXIT K ADC,ADCSTART
 Q
OPT ;EP;TO EDIT OPTION DESCRIPTIONS
 W !!?15,"EDIT OPTION DESCRIPTIONS"
 F  D OPT1 Q:$D(DIRUT)
 G EXIT
OPT1 S DIR(0)="PO^19,:AEMQ",DIR("A")="Select OPTION"
 W !
 D ^DIR K DIR
 Q:+Y<1
 S DA=+Y,DIE="^DIC(19,",DR="3.5"
 W !
 D ^DIE K DIE,DR,DA
 Q
SEC ;EP;TO EDIT SECURITY KEY DESCRIPTIONS
 W !!?21,"EDIT SECURITY KEY DESCRIPTIONS"
 F  D SEC1 Q:$D(DIRUT)
 G EXIT
SEC1 S DIR(0)="PO^19.1,:AEMQ",DIR("A")="Select SECURITY KEY"
 W !
 D ^DIR K DIR
 Q:+Y<1
 S DA=+Y,DIE="^DIC(19.1,",DR="1"
 W !
 D ^DIE K DIE,DR,DA
 Q
