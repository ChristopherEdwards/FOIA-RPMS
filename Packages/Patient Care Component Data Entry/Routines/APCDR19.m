APCDR19 ; IHS/CMI/LAB - CHECK V ACTIVITY TIME ;
 ;;2.0;IHS PCC SUITE;;MAY 14, 2009
 ;
ACT ;
 S APCDACT=$P(^AUPNVTM(APCDEDFN,0),U) I APCDACT=""!(+APCDACT'=APCDACT) S APCDE="E052" D ERR G XIT
 S APCDACT=$P(^AUPNVTM(APCDEDFN,0),U,4) G:APCDACT="" XIT
 I APCDACT'=+APCDACT S APCDE="E053" D ERR G XIT
XIT ;
 K APCDE,APCDACT
 Q
ERR ;
 S APCDE("FILE")=9000010.19,APCDE("ENTRY")=APCDEDFN
 D ERR^APCDRV
 Q
