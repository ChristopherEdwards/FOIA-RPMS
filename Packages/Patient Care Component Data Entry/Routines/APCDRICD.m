 ;;2.0;IHS PCC SUITE;**1**;MAY 14, 2009
 K APCDAGEE
EIN ; SCREEN OUT E CODES AND INACTIVE CODES
 I $E(APCDICD)="E" S APCDE="E047" Q
 I APCDICD=.9999 S APCDE="E026" Q
 ;S I=$P(^ICD9(APCDICDP,0),U,11) I I]"",$P($P($G(^AUPNVSIT(APCDVSIT,0)),U),".")>I S APCDE="E041" Q
 ;I $P(^ICD9(APCDICDP,0),U,9)]"" S APCDE="E041" Q
 G:$E(APCDICD)="." SEX I $L($P(APCDICD,".",2))>2 S APCDE="E003" Q
SEX ;
 NEW S,%
 S %=$$ICDDX^ICDCODE(APCDICDP) S S=$P(%,U,11)
 I S]"",AUPNSEX'=S S APCDE="E042" Q
AGE ; IF THERE IS AGE CRITERIA DATA AVAILABLE CHECK TO SEE THAT IT FITS THE CRITERIA
 S %=$$ICDDX^ICDCODE(APCDICDP)
 S (A,B)=""  ;CSV
 I $$VERSION^XPDUTL("BCSV")]"" D  I 1  ;CSV
 .S A=$P(%,U,15),B=$P(%,U,16)  ;CSV
 E   S A=$P($G(^ICD9(APCDICDP,9999999)),U),B=$P($G(^ICD9(APCDICDP,9999999)),U,2)
 I A]"",A>AUPNDAYS S APCDAGEE="" Q
 I B]"",B<AUPNDAYS S APCDAGEE="" Q
 Q
 ;
