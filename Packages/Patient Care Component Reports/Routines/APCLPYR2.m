 I APCLACT D
 .S X=X_"With eligibility "
 .I $G(APCLEDAT)=$G(APCLBDAT) S X=X_"as of "
 .I $G(APCLEDAT)'=$G(APCLBDAT) S X=X_"from "
 .S X=X_$E(APCLBDAT,4,5)_"/"_$E(APCLBDAT,6,7)_"/"_$E(APCLBDAT,2,3)
 .I $G(APCLEDAT)=$G(APCLBDAT) Q
