 S APCLHRN=$P(^AUPNPAT(DFN,41,APCLSU,0),U,2)
 S SSN=$P(^DPT(DFN,0),U,9)
 W !!,APCLHRN,?8,APCLPN,?40,DOB,?60,SSN
