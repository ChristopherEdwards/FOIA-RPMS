AZXZSUP4 ;SUPPORT DATABASE PROGRAM [ 05/01/95   1:25 PM ]
 ;05/01/92  JOHN H. LYNCH 
 ;
 ;THIS PROGRAM IS CREATED FOR THE PURPOSE
 ;OF CHECKING FOR ONLY "PENDING" LOGS AND
 ;EXCLUDING "WORKING" OR "CLOSED" LOGS.
 ;
 ;THIS PROGRAM IS CALLED FROM THE VARIABLE
 ;DIS(0) IN "Pending Log Print" OFF OF THE
 ;"Support Log Database" MENU IN THE CORE.
 
MAIN ;AZXZSUP4 PROGRAM CONTROL
 ;INITIALIZE LOCAL VARIABLES
 S FILE="1991012"          ;FILE NUMBER (SUPPDB)
 
 ;MAKE SURE STATUS FOR D0 (INTERNAL ENTRY #) IS ONLY "P"ending
 S STATUS="P",OCCUR=0 I $O(^DIZ(FILE,D0,1,"B",STATUS,OCCUR)) S STATUS="W",OCCUR=0 I '$O(^DIZ(FILE,D0,1,"B",STATUS,OCCUR)) S STATUS="C",OCCUR=0 I '$O(^DIZ(FILE,D0,1,"B",STATUS,OCCUR))
 K FILE,STATUS,OCCUR
 Q
