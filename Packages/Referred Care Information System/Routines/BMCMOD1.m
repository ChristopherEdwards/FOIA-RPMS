 S DIC(0)="L",DIC="^BMCPX(",DLAYGO=90001.02 D FILE^DICN
 I +Y<0 W !,"Unable to add CPT Procedure code." S BMCQ=1 Q
 S DR=".02////"_BMCDFN_";.03////"_BMCRIEN_";.04////P"_";.05;.06"
 S DIE="^BMCPX("
 D DIE^BMCFMC
 K DLAYGO,BMCDX,DIC,DIE,DR,DA,X
 Q
