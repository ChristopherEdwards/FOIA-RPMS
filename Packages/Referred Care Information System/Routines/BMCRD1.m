BMCRD1 ; IHS/PHXAO/TMJ - NO DESCRIPTION PROVIDED ;  
 ;;4.0;REFERRED CARE INFO SYSTEM;;JAN 09, 2006
START ;
 D GETREF
 Q:BMCQ
 D BROWSE
 D XIT
 Q
 ;
GETREF ; GET REFERRAL
 S BMCQ=1
 S DIC="^BMCREF(",DIC(0)="AEMQ" D DIC^BMCFMC
 Q:Y<0
 S BMCRIEN=+Y
 S BMCQ=0
 Q
 ;
BROWSE ;
 S XBRP="DISP^BMCRD1"
 S XBRC="",XBRX="XIT^BMCRD1",XBIOP=0 D ^XBDBQUE
 ;D VIEWR^XBLM("^BMCVLP")
 Q
XIT ;EP
 D ^BMCKILL
 K ^TMP("BMCRDSP",$J)
 Q
DISP ;EP
 D EN^VALM("BMC RECORD DISPLAY")
 D CLEAR^VALM1
 Q
HDR ; -- header code
 S VALMHDR(1)="User:  "_$P(^VA(200,DUZ,0),U)
 Q
 ;
INIT ; -- init variables and list array
 D EP^BMCRD(BMCRIEN)
 S VALMCNT=$O(^TMP("BMCRDSP",$J,""),-1)
 Q
 ;
HELP ; -- help code
 S X="?" D DISP^XQORM1 W !!
 Q
 ;
EXIT ; -- exit code
 Q
