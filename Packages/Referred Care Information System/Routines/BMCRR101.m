 Q
PROC ;
 S BMCRREC=^BMCREF(BMCREF,0),DFN=$P(BMCRREC,U,3)
 Q:$P(BMCRREC,U,4)="N"
 Q:$P(BMCRREC,U,15)'="A"
 ;get sort value
 S BMCFAC=$$FACREF^BMCRLU(BMCREF) S:BMCFAC="" BMCFAC="???"
 S ^XTMP("BMCRR10",BMCJOB,BMCBTH,"DATA HITS",BMCFAC,BMCREF)="",BMCRCNT=BMCRCNT+1
 Q
