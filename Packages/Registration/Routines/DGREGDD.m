DGREGDD ;ALB/REW - REGISTRATION PATIENT FILE MUMPS X-REFS ; 30-MAR-93
 ;;5.3;Registration;;Aug 13, 1993
 ;
 ; FOR CFL2 & CFL4 TAGS:
 ; INPUT:
 ;    X  = VALUE OF ORIGINAL FIELD
 ; OUTPUT: 
 ;   DGX = VALUE OF TARGETED FIELD (USED IN SET/KILL STATEMENT)
 ;
CFL2 ;CFL FIELD (#.312) SETS DGX FOR (#.314) FIELD
 S DGX=$O(^DIC(4,"D",+X,0))
 Q
CFL4 ;CFL FIELD (#.314) SETS DGX FOR (#.312) FIELD
 N DGNM,DGST
 S DGX=""
 S DGNM=$P($G(^DIC(4,X,0)),U,1)
 S DGST=$P($G(^DIC(4,X,99)),U,1)
 S:DGST DGX=$E(DGST_DGNM,1,40)
 Q
