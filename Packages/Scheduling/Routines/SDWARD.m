 D HED
 ;
 ;IHS/ANMC/LJF 5/17/01 changed $N to $O
 ;S SDD=0 F SD=0:0 S SDD=$N(^UTILITY("SD",$J,SDD)) Q:SDD<0  S DFN=$N(^UTILITY("SD",$J,SDD,0)) D PN F SDI=0:0 S SDI=$N(^UTILITY("SD",$J,SDD,DFN,SDI)) Q:SDI<0  F SC=0:0 S SC=$N(^UTILITY("SD",$J,SDD,DFN,SDI,SC)) Q:SC<0  D PRT
 S SDD=0 F SD=0:0 S SDD=$O(^UTILITY("SD",$J,SDD)) Q:SDD=""  S DFN=$O(^UTILITY("SD",$J,SDD,0)) Q:'DFN  D PN F SDI=0:0 S SDI=$O(^UTILITY("SD",$J,SDD,DFN,SDI)) Q:SDI=""  F SC=0:0 S SC=$O(^UTILITY("SD",$J,SDD,DFN,SDI,SC)) Q:SC=""  D PRT
 ;IHS/ANMC/LJF 5/17/01 end of changes
 ;
 G END
CHECK Q:'$D(^DPT(DFN,"S"))
 ;
 ;IHS/ANMC/LJF 5/17/01 changed $N to $O
 ;F SDI=DT-.1:0 S SDI=$N(^DPT(DFN,"S",SDI)) Q:SDI<0  I $P(^(SDI,0),"^",2)'["C" S ^UTILITY("SD",$J,$P(^DPT(DFN,0),"^",1),DFN,SDI,+^DPT(DFN,"S",SDI,0))=""
 F SDI=DT-.1:0 S SDI=$O(^DPT(DFN,"S",SDI)) Q:'SDI  I $P(^(SDI,0),"^",2)'["C" S ^UTILITY("SD",$J,$P(^DPT(DFN,0),"^",1),DFN,SDI,+^DPT(DFN,"S",SDI,0))=""
 ;IHS/ANMC/LJF 5/17/01 end of changes
 ;
 Q
PRT D:$Y+2>IOSL HED
 W !,?3,$S($D(^SC(SC,0)):$P(^(0),"^",1),1:"DELETED CLINIC")
 S Y=SDI\1 D D^DIQ W ?50,Y," " S X=SDI D TM^SDROUT0 W ?61,$J(X,8)
