 ;;3.0; Surgery ;**68,50**;24 Jun 93
 I '$D(SRSITE) D ^SROVAR S SRSITE("KILL")=1
 S SRSOUT=0 I '$D(SRTN) K SRNEWOP D ^SROPS G:'$D(SRTN) END
 K %ZIS,IO("Q") S %ZIS="Q" D ^%ZIS G:POP END I $D(IO("Q")) K IO("Q") S ZTRTN="SRONIN1",ZTDESC="NURSE INTRAOPERATIVE REPORT",(ZTSAVE("SRTN"),ZTSAVE("SRSITE*"))="" D ^%ZTLOAD G END
 D ^SRONIN1 Q
END D ^SRSKILL W @IOF D ^%ZISC I $D(SRSITE("KILL")) K SRSITE
 Q
