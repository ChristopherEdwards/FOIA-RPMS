START ;START
 K ^DD(80.1,0,"DIC")
 D ICD0
 D REV
 S ^DD(80.1,0,"DIC")="AICDKWI0"
 Q
ICD0 ;install new icd0 codes
 F AUMI=124:1:132 D
 .S AUMLN=$T(@AUMI)
 .F AUMJ=2:1:8 S AUM(AUMJ)=$P(AUMLN,";",AUMJ)
 .S Y=$O(^ICD0("AB",AUM(2),0))
 .I 'Y D
 ..S DIC="^ICD0("
 ..S DIC(0)="LX"
 ..S X=AUM(2)
 ..D ^DIC
 .Q:+Y<0  S DA=+Y
 .S ^ICD0(DA,1)=AUM(3)
 .S:AUM(4)'="" $P(^ICD0(DA,0),"^",10)=AUM(4)
 .S $P(^ICD0(DA,9999999),"^",4)=3001000
 .D MDC
 .S DIK="^ICD0(" D IX1^DIK
 Q
MDC ;update MDC multiple
 F AUMI2=1:1 S AUMLN=$T(@AUMI+AUMI2) Q:+AUMLN  D
 .S AUMMDC=$P(AUMLN,";",2)
 .S AUMDRG=$P(AUMLN,";",3)
 .S ^ICD0(DA,"MDC",AUMMDC,0)=AUMMDC
 .S ^ICD0(DA,"MDC",AUMMDC,"DRG")=AUMDRG
 S:+AUMLN'=999 ^ICD0(DA,"MDC",0)="^80.12PA^"_AUMMDC_"^"_(AUMI2-1)
 K AUMI2,AUMMDC,AUMDRG
 Q
