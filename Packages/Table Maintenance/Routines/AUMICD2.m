 .S AUMCT=AUMCT+1
 .S DA=$O(^ICD0("AB",I,0))
 .Q:'DA
 .S ^ICD0(DA,1)=AUM(AUMCT)
 .S DIK="^ICD0(" D IX1^DIK
 .Q:I=86.59
