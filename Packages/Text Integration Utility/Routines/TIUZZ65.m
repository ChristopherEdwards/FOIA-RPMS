CLXREF ; Reset 8925 cross-reference ACLPT05
 S %ZIS="Q" D ^%ZIS I POP G CLXRX
 I '$D(IO("Q")) G CLXREF1
 S ZTRTN="CLXREF1^TIUZZ65",ZTDESC="TIU Reset XREF ACLPT05"
 D ^%ZTLOAD G CLXRX
 ;
CLXREF1 N DIK
 S DIK="^TIU(8925,"
 S DIK(1)=".05^ACLPT05" D ENALL^DIK
 U IO
 W !!,"  ... Done Resetting TIU XREF ACLPT05",!!
 ;
CLXRX D ^%ZISC,HOME^%ZIS
 I $D(ZTQUEUED) S ZTREQ="@"
 Q
