 ; IHS/SD/SDR - v2.5 p8 - IM15585
 ;    Check for new patient, not just new subscriber
 ;
 ; IHS/SD/SDR - v2.5 p8 - IM13177/IM13285
 ;    Change so DOB comes from page 4 of Reg
 ;
 ; IHS/SD/SDR - v2.5 p10 - IM19923
 ;   Don't write address if RCID=610442
 ;
START ;START HERE
 S ABMI=1
 D EP^ABME8HL(22,+$G(ABMCHILD))
 D WR^ABMUTL8("HL")
 D EP^ABME8SBR(ABMPSQ)
 D WR^ABMUTL8("SBR")
 D EP^ABME8NM1("IL")
 D WR^ABMUTL8("NM1")
 D EP^ABME8N3(ABMSFILE,ABMSIEN)
 D WR^ABMUTL8("N3")
 D EP^ABME8N4(ABMSFILE,ABMSIEN)
 D WR^ABMUTL8("N4")
 D EP^ABME8DMG(ABMSFILE,ABMSIEN)
 D WR^ABMUTL8("DMG")
 S ABMLOOP="2010BB"
 D EP^ABME8NM1("PR",ABMP("INS"))
 D WR^ABMUTL8("NM1")
 I $$RCID^ABMUTLP(ABMP("INS"))'=610442 D
 .D EP^ABME8N3(9999999.18,ABMP("INS"))
 .D WR^ABMUTL8("N3")
 .D EP^ABME8N4(9999999.18,ABMP("INS"))
 .D WR^ABMUTL8("N4")
 Q:'ABMCHILD
PTCHG ;EP
 S ABMI=1
 D EP^ABME8HL(23,0)
 D WR^ABMUTL8("HL")
 D ^ABME8PAT
 D WR^ABMUTL8("PAT")
 D EP^ABME8NM1("QC")
 D WR^ABMUTL8("NM1")
 D EP^ABME8N3(2,ABMP("PDFN"))
 D WR^ABMUTL8("N3")
 D EP^ABME8N4(2,ABMP("PDFN"))
 D WR^ABMUTL8("N4")
 D EP^ABME8DMG(2,ABMP("PDFN"))
 D WR^ABMUTL8("DMG")
 Q
