EN(X) ;EP
 S ABM1("X")=$X
 I '$G(IOST(0)) S IOP=0 D ^%ZIS
 I '$D(ABMVDF(+IOST(0),X)) D
 .S ABM1("LN")=$T(@X),ABM1(1)=$P(ABM1("LN"),";;",2),ABM1(2)=$P(ABM1("LN"),";;",3),ABM1(3)=$P(ABM1("LN"),";;",4)
 .S ABMVDF(+IOST(0),X)=$P($G(^%ZIS(2,+IOST(0),ABM1(1))),"^",ABM1(2),ABM1(3))
 .I ABMVDF(+IOST(0),X)="" S ABMVDF(+IOST(0),X)="*0"
 W @ABMVDF(+IOST(0),X)
 S $X=ABM1("X")
 S X="" K ABM1 Q X
