 S ABPAHD1="Print PAYMENT BATCH STATISTICAL REPORT" D HEADER^ABPAMAIN
 D ^ABPADATE G:$D(EDT)'=1 END
 S ABPA("DTIN")=BDT D DTCVT^ABPAMAIN S FR=ABPA("DTOUT")
 S ABPA("DTIN")=EDT D DTCVT^ABPAMAIN S TO=ABPA("DTOUT")
 S L=0,DIC="^ABPAPBAT(",BY="[ABPA/BATCH/STATISTICS]"
 S DHD=ABPATLE_" - BATCH STATISTICS - "_FR_" to "_TO
 S %ZIS("A")="Select DEVICE or [Q]ueue: "
 W ! D EN1^DIP D PAUSE^ABPAMAIN
END K BDT,DIC,EDT,X,Y,FR,TO,L,BY,DHD,%ZIS("A")
