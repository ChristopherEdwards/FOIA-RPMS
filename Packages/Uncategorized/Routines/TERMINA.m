TERMINA ; GENERAL I/O TO COMMUNICATIONS PORT [ 09/19/85  2:58 PM ]
OPEN S %PORT=68,X=0 O %PORT
READ ;READ SCREEN
 R !,%RESP S %PORT=68 U %PORT
 S %XS=""
 U %PORT W:$L(%RESP) %RESP W *13
PORTIO ; WRITE TO PORT
 U %PORT R %Y:0 S %CR=$ZB U 0 W:$L(%Y) !,%Y W:$T $C(%CR)
 S %XS=%XS_%Y
 I X<200 S X=0
 E  G READ
WRITE U 0 W !,%Y
 G READ
PORTOK Q
PORTERR U 0 W !,%ERRMSG,%XS G EXIT
END ;END OF ROUTINE
