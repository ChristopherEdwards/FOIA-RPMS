 S DIE="^AUPNVPED("
 S MIN=""
 I TIME,CNT S MIN=TIME/CNT
 I MIN S MIN=$J(MIN,1,0) ; MUST BE AN INTEGER ; PATCHED BY GIS 1/18/2011
 S DA=0
