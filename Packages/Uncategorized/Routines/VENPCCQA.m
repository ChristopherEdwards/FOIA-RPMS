WCE ; ******************** WELL CHILD EXAM * 9000010 *******
 N LINE,ARR
 ; <SETUP>
 X APCHSCKP Q:$D(APCHSQIT)  X:'APCHSNPG APCHSBRK
 ; <DISPLAY>
 D WCESEG(APCHSPAT) ; CREATE THE DISPLAY ARRAY
 D PRINT ; PRINT THE SEGMENT
 ; <CLEANUP>
CLEANUP K APCHSDFN,APCHSN,APCHSICD,APCHSDAT,APCHSNRQ,APCHSICL
