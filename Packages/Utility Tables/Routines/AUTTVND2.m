 I AUTPREF=1,$G(X)="" D
 .I '$D(ZTQUEUED) W !,"EIN SUFFIX is required for Vendors of type ORGANIZATION."
 .K X
 Q:'$D(X)
 D SUFFIX^AUTTVNDR
