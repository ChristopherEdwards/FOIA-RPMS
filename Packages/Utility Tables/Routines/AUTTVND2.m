 Q:$D(DDS)&($G(DDS)["ACR")
 I X["""" K X Q
 I $L(X)>2 K X Q
 I $L(X)<2 K X Q
 I $A(X)=45 K X Q
 I X'?2UN K X Q
 N AUTEIN,AUTPREF
 S AUTEIN=$P($G(^AUTTVNDR(DA,11)),"^",1)
