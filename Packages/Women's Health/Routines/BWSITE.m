 ;;  CALLED BY OPTION: "BW EDIT SITE PARAMETERS".
 ;
 D EDIT
 ;
EXIT ;EP
 W:$D(IOF) @IOF
 D KILLALL^BWUTL8
 Q
 ;
EDIT ;EP
 D SETVARS^BWUTL5
 N A,B
 D TITLE^BWUTL5("EDIT SITE PARAMETERS") W !
 S A="   Select SITE/FACILITY: ",B=""
 S:$D(DUZ(2)) B=$$INSTTX^BWUTL6(DUZ(2))
 D DIC^BWFMAN(9002086.02,"QEMAL",.Y,A,B)
 Q:Y<0
 ;---> EDIT WITH SCREENMAN.
 D DDS^BWFMAN(9002086.02,"[BW SITE PARAMS-FORM-1]",+Y)
 Q
 ;
DIAGMODE ;EP
 ;---> EDIT PCC DIAGNOSTIC MODE SITE PARAMETER.
 ;---> CALLED BY OPTION "BW PCC EDIT DIAG MODE".
 D SETVARS^BWUTL5
 D TITLE^BWUTL5("RUN PCC IN DIAGNOSTIC MODE") W !
 S A="   Select SITE/FACILITY: ",B=""
 S:$D(DUZ(2)) B=$$INSTTX^BWUTL6(DUZ(2))
 D DIC^BWFMAN(9002086.02,"QEMAL",.Y,A,B)
 Q:Y<0
 ;---> EDIT WITH SCREENMAN.
 D DIE^BWFMAN(9002086.02,".2//NO",+Y)
 Q
